/**
 * Coordinates of a location on earth using Spherical Mercator Projection.
 *
 * Latitude (north-south) and Longitude (east-west) angles are both
 * in decimal degrees.
 *
 * Latitude 90.0 is North Pole, -90.0 is South Pole
 * Longitude 0.0 matches Greenwich and goes up to 180.0 to the east and -180.0 to the west
 */
export interface Coordinates {
	Latitude: number;
	Longitude: number;
}

